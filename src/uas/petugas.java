    private void updateDataPetugas(String namaPetugas, String no_hpPetugas, String alamatPetugas, String namaPetugasBaru, String no_hpPetugasBaru, String alamatPetugasBaru) {
        String query = "UPDATE petugas SET nama = ?, no_hp = ?, alamat = ? WHERE nama = ? AND no_hp = ? AND alamat = ?";

            try {
                PreparedStatement statement = connection.prepareStatement(query);
                statement.setString(1, namaPetugasBaru);
            statement.setString(2, no_hpPetugasBaru);
            statement.setString(3, alamatPetugasBaru);
            statement.setString(4, namaPetugas);
            statement.setString(5, no_hpPetugas);
            statement.setString(6, alamatPetugas);
            statement.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    } 
    
    private void hapusDataPetugas(int rowIndex) {
        String namaPetugas = tableModel.getValueAt(rowIndex, 0).toString();
        String no_hpPetugas = tableModel.getValueAt(rowIndex,1).toString();
        String alamatPetugas = tableModel.getValueAt(rowIndex, 2).toString();

        String query = "DELETE FROM petugas WHERE nama = ? AND no_hp = ? AND alamat = ?" ;

        try {
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, namaPetugas);
            statement.setString(2, no_hpPetugas);
            statement.setString(3, alamatPetugas);
            statement.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void simpanDatabasePetugas(String namaPetugas, String no_hpPetugas, String alamatPetugas){
        String query = "INSERT INTO petugas (nama, no_hp, alamat) VALUES (?, ?, ?)";

        try {
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, namaPetugas);
            statement.setString(2, no_hpPetugas);
            statement.setString(3, alamatPetugas);
            statement.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void initDatabase() {
        String url = "jdbc:mysql://localhost:3306/toko_abdi_jaya";
        String username = "root";
        String password = "";

        try {
            connection = DriverManager.getConnection(url, username, password);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void initTableModel(){
        String [] columnName = {"Nama","no_hp","alamat"};
        tableModel = new DefaultTableModel(columnName,0);
        output_petugas.setModel(tableModel);
        mengambilDatabase();
    }
    
    private void mengambilDatabase() {
        String query = "SELECT nama, no_hp,alamat FROM petugas";

        try {
            PreparedStatement statement = connection.prepareStatement(query);
            ResultSet resultSet = statement.executeQuery();

            while (resultSet.next()) {
                String namaPetugas = resultSet.getString("nama");
                String no_hpPetugas = resultSet.getString("no_hp");
                String alamatPetugas = resultSet.getString("alamat");
                Object[] row = {namaPetugas, no_hpPetugas,alamatPetugas};
                tableModel.addRow(row);
            }

            resultSet.close();
            statement.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
