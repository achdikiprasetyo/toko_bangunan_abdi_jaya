    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = output_petugas.getSelectedRow();
        if (selectedRow != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                hapusDataPetugas(selectedRow); // Hapus dari database
                tableModel.removeRow(selectedRow); // Hapus dari tabel
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus terlebih dahulu", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hapusActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        int selectedRow = output_petugas.getSelectedRow();
        if (selectedRow != -1) {
            String namaPetugas = tableModel.getValueAt(selectedRow, 0).toString();
            String no_hpPetugas = tableModel.getValueAt(selectedRow, 1).toString();
            String alamatPetugas = tableModel.getValueAt(selectedRow, 2).toString();

            // Tampilkan dialog atau form untuk mengedit data, dan simpan perubahannya dalam variabel namaBarangBaru dan hargaBarangBaru

            String namaPetugasBaru = JOptionPane.showInputDialog(this, "Masukkan Nama Petugas Baru", namaPetugas);
            String no_hpPetugasBaru = JOptionPane.showInputDialog(this, "Masukkan no hp Baru", no_hpPetugas);
            String alamatPetugasBaru = JOptionPane.showInputDialog(this, "Masukkan alamat Baru", alamatPetugas);

            // Perbarui data dalam tabel
            tableModel.setValueAt(namaPetugasBaru, selectedRow, 0);
            tableModel.setValueAt(no_hpPetugasBaru, selectedRow, 1);
            tableModel.setValueAt(alamatPetugasBaru, selectedRow, 2);

            // Perbarui data dalam database
            updateDataPetugas(namaPetugas, no_hpPetugas, alamatPetugas, namaPetugasBaru, no_hpPetugasBaru, alamatPetugasBaru);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit terlebih dahulu", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditActionPerformed

    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        String namaPetugas = nama_petugas.getText();
        String no_hpPetugas= no_hp.getText();
        String alamatPetugas = alamat_petugas.getText();

        if (!namaPetugas.isEmpty() && !no_hpPetugas.isEmpty()&& !alamatPetugas.isEmpty()) {

            // Tambahkan baris baru ke dalam model tabel
            Object[] row = {namaPetugas, no_hpPetugas, alamatPetugas};
            tableModel.addRow(row);

            // Kosongkan input setelah ditambahkan ke tabel
            nama_petugas.setText("");
            no_hp.setText("");
            alamat_petugas.setText("");

            // Simpan data ke database
            simpanDatabasePetugas(namaPetugas, no_hpPetugas, alamatPetugas);
        } else {
            JOptionPane.showMessageDialog(this, "Isi Nama , no hp dan alamat petugas terlebih dahulu", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tambahActionPerformed

